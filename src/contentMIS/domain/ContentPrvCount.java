package contentMIS.domain;

public class ContentPrvCount {
	
	public String content_prv;
	public String name;
	public int count;
	
	
	public String getContent_prv() {
		return content_prv;
	}
	public void setContent_prv(String content_prv) {
		this.content_prv = content_prv;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	

}
